package main

import "fmt"

func main() {
	// Explicit declaration
	var name string = "Sang"
	var age int = 33

	// Type inference
	city := "Ho Chi Minh"
	year := 2025

	// Constant declaration
	const Pi = 3.14

	// Print values
	fmt.Println("Name:", name)
	fmt.Println("Age:", age)
	fmt.Println("City:", city)
	fmt.Println("Year:", year)
	fmt.Println("Pi:", Pi)
}
